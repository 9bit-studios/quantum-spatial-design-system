// Service Package Cards with Pricing
export function servicePackageCard(Component, categoryIndex, packageIndex) {
    return function ServicePackageCard(props) {
        const category = comprehensiveRebrandingContent.services.detailed_services.categories[categoryIndex];
        const package = category?.packages[packageIndex];
        
        if (!package) return Component(props);
        
        return Component({
            ...props,
            name: package.name,
            price: package.price,
            description: package.description,
            features: package.features,
            category: category.category,
            isPopular: packageIndex === 1 // Middle package is usually popular
        });
    };
}

