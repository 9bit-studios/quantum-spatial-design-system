// Comprehensive Design System Application
export const quantumSpatialDesignSystem = comprehensiveRebrandingContent.brand.design_system;

export function applyQuantumDesignSystem(style, element = 'default') {
    const ds = quantumSpatialDesignSystem;
    
    const baseStyle = {
        fontFamily: ds.typography_system.body,
        color: ds.color_scheme.text.primary
    };
    
    const elementStyles = {
        heading: {
            fontFamily: ds.typography_system.heading,
            background: `linear-gradient(45deg, ${ds.color_scheme.accent}, ${ds.color_scheme.primary})`,
            WebkitBackgroundClip: 'text',
            WebkitTextFillColor: 'transparent',
            backgroundClip: 'text'
        },
        card: {
            background: `linear-gradient(135deg, ${ds.color_scheme.primary}20, ${ds.color_scheme.secondary}10)`,
            backdropFilter: 'blur(20px)',
            border: `1px solid ${ds.color_scheme.accent}30`,
            borderRadius: '12px',
            padding: ds.spacing.md
        },
        button: {
            background: `linear-gradient(135deg, ${ds.color_scheme.primary}, ${ds.color_scheme.secondary})`,
            border: 'none',
            borderRadius: '8px',
            padding: `${ds.spacing.sm} ${ds.spacing.md}`,
            color: ds.color_scheme.text.primary,
            fontFamily: ds.typography_system.heading,
            fontWeight: '600'
        }
    };
    
    return {
        ...baseStyle,
        ...elementStyles[element],
        ...style
    };
}

// Pricing Display Utilities
export function formatPricingPackage(category, packageIndex) {
    const pricing = comprehensiveRebrandingContent.pricing;
    const categoryData = pricing[category.toLowerCase().replace(' ', '_')];
    const package = categoryData?.packages[packageIndex];
    
    if (!package) return null;
    
    return {
        ...package,
        formattedPrice: package.price === "Custom pricing" ? "Custom" : package.price,
        isCustom: package.price.includes("Custom"),
        category: categoryData.category,
        startingPrice: categoryData.starting_price
    };
}

// Content Enhancement Utilities
export function enhanceTextContent(text, enhancement = 'quantum') {
    const enhancements = {
        quantum: text => text.replace(/\b(innovation|design|experience)\b/gi, match => 
            `quantum-spatial ${match.toLowerCase()}`),
        apple: text => text.replace(/\b(AI|intelligence)\b/gi, 'Apple Intelligence'),
        privacy: text => text.replace(/\b(secure|safe)\b/gi, 'privacy-first')
    };
    
    return enhancements[enhancement] ? enhancements[enhancement](text) : text;
}

